import org.yaml.snakeyaml.Yaml;

public class Ice
    extends Asteroid
{
    /**
     * Number of items needed to be able to compress it
     */
    @HighIsGood(true)
    @Stackable(true)
    @DefaultIntValue(0)
    public int compressionquantityneeded;
    /**
     * What type this type can be compressed into
     */
    @HighIsGood(true)
    @Stackable(true)
    @DefaultIntValue(0)
    public int compressiontypeid;
    /**
     * Reference for grouping ores in visual displays. All variants of one ore should have the same BasicType ID
     */
    @HighIsGood(false)
    @Stackable(false)
    @DefaultIntValue(0)
    public int orebasictype;
    /**
     * The type ID of the skill that is required.
     */
    @HighIsGood(true)
    @Stackable(true)
    @DefaultIntValue(0)
    public int requiredskill1;
    /**
     * Required skill level for skill 1
     */
    @HighIsGood(true)
    @Stackable(true)
    @DefaultIntValue(0)
    public int requiredskill1level;
    /**
     * Resistance against Stasis Webifiers
     */
    @HighIsGood(false)
    @Stackable(true)
    @DefaultDoubleValue(1.0)
    public double stasiswebifierresistance;
    public static final Set<Attribute> ATTRIBUTES = Collections.unmodifiableSet(new LinkedHashSet<>(Arrays.asList(new Attribute[] {Radius.INSTANCE, StasisWebifierResistance.INSTANCE, Mass.INSTANCE, Capacity.INSTANCE, AsteroidMetaLevel.INSTANCE, CompressionTypeID.INSTANCE, RequiredSkill1Level.INSTANCE, CompressionQuantityNeeded.INSTANCE, RequiredSkill1 .INSTANCE, ReprocessingSkillType.INSTANCE, OreBasicType.INSTANCE, AsteroidRadiusGrowthFactor.INSTANCE, AsteroidRadiusUnitSize.INSTANCE })));
    public static final Ice.MetaGroup METAGROUP = new Ice.MetaGroup();

    @Override
    public Number attribute(Attribute attribute) {
        switch (attribute.getId()) {
            case  1941 :
            {
                return compressionquantityneeded;
            }
            case  1940 :
            {
                return compressiontypeid;
            }
            case  2711 :
            {
                return orebasictype;
            }
            case  182 :
            {
                return requiredskill1;
            }
            case  277 :
            {
                return requiredskill1level;
            }
            case  2115 :
            {
                return stasiswebifierresistance;
            }
            default:
            {
                return super.attribute((attribute));
            }
        }
    }

    @Override
    public Set<Attribute> getAttributes() {
        return ATTRIBUTES;
    }

    @Override
    public IMetaGroup<Ice> getGroup() {
        return METAGROUP;
    }

    public static class MetaGroup
        implements IMetaGroup<Ice>
    {
        public static final String RESOURCE_PATH = "SDE/types/asteroid/Ice.yaml";
        private Map<String, Ice> cache = (null);

        @Override
        public IMetaCategory<? super Ice> category() {
            return Asteroid.METACAT;
        }

        @Override
        public int getGroupId() {
            return  465;
        }

        @Override
        public String getName() {
            return "Ice";
        }

        @Override
        public synchronized Map<String, Ice> load() {
            if (cache == null) {
                try(final InputStreamReader reader = new InputStreamReader(Ice.MetaGroup.class.getClassLoader().getResourceAsStream((RESOURCE_PATH)))) {
                    cache = new Yaml().loadAs(reader, (Container.class)).types;
                } catch (final Exception exception) {
                    throw new UnsupportedOperationException("catch this", exception);
                }
            }
            return Collections.unmodifiableMap(cache);
        }

        private static class Container {
            public LinkedHashMap<String, Ice> types;
        }
    }
}
