public class Mercoxit
    extends Asteroid
{
    /**
     * %chance of new asteroid releasing damage cloud each mining turn.
     */
    @HighIsGood(true)
    @Stackable(true)
    @DefaultRealValue(0.0)
    public double damagecloudchance;
    /**
     * The type of damage cloud generated by the asteroid.
     */
    @HighIsGood(true)
    @Stackable(true)
    @DefaultIntValue(0)
    public int damagecloudtype;
    @HighIsGood(false)
    @Stackable(false)
    @DefaultIntValue(0)
    public int ignoreminingwaste;
    /**
     * Reference for grouping ores in visual displays. All variants of one ore should have the same BasicType ID
     */
    @HighIsGood(false)
    @Stackable(false)
    @DefaultIntValue(0)
    public int orebasictype;
    /**
     * Resistance against Stasis Webifiers
     */
    @HighIsGood(false)
    @Stackable(true)
    @DefaultRealValue(1.0)
    public double stasiswebifierresistance;
    public static final Set<Attribute> ATTRIBUTES = Collections.unmodifiableSet(new LinkedHashSet<>(Arrays.asList(new Attribute[] {DamageCloudType.INSTANCE, Radius.INSTANCE, StasisWebifierResistance.INSTANCE, IgnoreMiningWaste.INSTANCE, Capacity.INSTANCE, DamageCloudChance.INSTANCE, AsteroidMetaLevel.INSTANCE, RequiredSkill1Level.INSTANCE, RequiredSkill1 .INSTANCE, ReprocessingSkillType.INSTANCE, OreBasicType.INSTANCE, AsteroidRadiusGrowthFactor.INSTANCE, AsteroidRadiusUnitSize.INSTANCE })));
    public static final Mercoxit.MetaGroup METAGROUP = new Mercoxit.MetaGroup();

    @Override
    public Number valueSet(Attribute attribute) {
        switch (attribute.getId()) {
            case  522 :
            {
                return damagecloudchance;
            }
            case  546 :
            {
                return damagecloudtype;
            }
            case  3236 :
            {
                return ignoreminingwaste;
            }
            case  2711 :
            {
                return orebasictype;
            }
            case  2115 :
            {
                return stasiswebifierresistance;
            }
            default:
            {
                return super.valueSet((attribute));
            }
        }
    }

    @Override
    public Set<Attribute> getAttributes() {
        return ATTRIBUTES;
    }

    @Override
    public IMetaGroup<Mercoxit> getGroup() {
        return METAGROUP;
    }

    public static class MetaGroup
        implements IMetaGroup<Mercoxit>
    {
        public static final String RESOURCE_PATH = "SDE/types/asteroid/Mercoxit.yaml";
        private Map<String, Mercoxit> cache = (null);

        @Override
        public IMetaCategory<? super Mercoxit> category() {
            return Asteroid.METACAT;
        }

        @Override
        public int getGroupId() {
            return  468;
        }

        @Override
        public String getName() {
            return "Mercoxit";
        }

        @Override
        public synchronized Map<String, Mercoxit> load() {
            if (cache == null) {
                try(final InputStreamReader reader = new InputStreamReader(Mercoxit.MetaGroup.class.getClassLoader().getResourceAsStream((RESOURCE_PATH)))) {
                    cache = new Yaml().loadAs(reader, (Container.class)).types;
                } catch (final Exception exception) {
                    throw new UnsupportedOperationException("catch this", exception);
                }
            }
            return Collections.unmodifiableMap(cache);
        }

        private static class Container {
            public LinkedHashMap<String, Mercoxit> types;
        }
    }
}
